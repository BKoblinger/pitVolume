pitGeomS.ellipseB = nan;
pitGeomS.ellipsePhi = nan;
pitGeomS.ellipseXc = nan;
pitGeomS.ellipseYc = nan;
pitGeomS.independentEllipse = [];

if nargin == 1 && ~isempty(pitGS)
    pitGeomS.label = pitGS.label;
    pitGeomS.file = pitGS.file;
    pitGeomS.independentEllipse = pitGS.independentEllipse;
end

end